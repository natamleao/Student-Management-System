                printf("+ INFORME O NOME DO LIVRO: ");
                fgets(_title, 100, stdin);
                _title[strcspn(_title, "\n")] = 0;
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ INFORME O NÚMERO DE PÁGINAS DO LIVRO: ");
                scanf("%zu", &_pagesNumber);
