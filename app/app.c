        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 1 - MATRÍCULAR ALUNO(A) --------------------------------------------------+\n");
        printf("+---------------------------------------------------------------------------+\n");
        printf("+ 2 - CANCELAR MATRÍCULA DO ALUNO(A) ---------------------------------------+\n");
