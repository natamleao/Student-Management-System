            case 4:
                if(ListGetSize(list) == 0){
                    printf("+---------------------------------------------------------------------------+\n");
                    printf("+ LISTA VAZIA --------------------------------------------------------------+\n");
                    printf("+---------------------------------------------------------------------------+\n");
                
                    break;
                }
                printf("+---------------------------------------------------------------------------+\n");
                printf("+--------------------- ATUALIZAR DADOS DO ALUNO(A) -------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");
                printf("+ A PLAYLIST FOI ORDENADA --------------------------------------------------+\n");
                printf("+---------------------------------------------------------------------------+\n");

                break;

